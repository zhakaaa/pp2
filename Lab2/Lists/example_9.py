#COPY LIST

# Example 9.1 : The method "copy" will copy all values from first to second list
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)   #['apple', 'banana', 'cherry']


# Example 9.2 : Another way to copy values is by method "list"
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)   #["apple", "banana", "cherry"]
